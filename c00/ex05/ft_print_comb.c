#include <unistd.h>

void	ft_print_comb(void)
{
	char	a[3];

	a[0] = '0';
	a[1] = '0';
	a[2] = '0';
	while (a[0] <= '7')
	{
		while (a[1]++ <= '8')
		{
			a[2]++;
			while (a[2]++ < '9')
			{
				write(1, &a[0], 1);
				write(1, &a[1], 1);
				write(1, &a[2], 1);
				if (!(a[0] == '7' && a[1] == '8'))
					write(1, ", ", 2);
			}
			a[2] = a[1];
		}
		a[0]++;
		a[1] = a[0];
		a[2] = a[0];
	}
}
